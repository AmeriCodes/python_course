"""
Python = Programming language
Type of typing = Dynamic / Strong
str -> string -> text
Strings are texts that are enclosed in quotation marks
"""
print(1234)

# Single quotes
print('Mateus Américo')
print(1, 'Mateus "Américo"')

# Doble quotes
print("Mateus \"Américo")
print(1, "Mateus 'Américo'")

# Escape
print("Mateus \"Américo\"")

# r
print(r"Mateus \"Américo\"")
